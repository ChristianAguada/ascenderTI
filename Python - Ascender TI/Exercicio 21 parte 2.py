alturaChico = 150
alturaJuca = 110
anos = 0

while alturaJuca <= alturaChico:
    alturaChico = alturaChico + 2
    alturaJuca = alturaJuca + 3
    anos = anos + 1

print('Foram necessários ao todo,', anos, 'para Juca alcançar Chico')
print('A altura atual de Juca é:', alturaJuca, 'e a altura atual de Chico é:', alturaChico)
